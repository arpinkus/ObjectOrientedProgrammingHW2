package student.part_h;

public class Worker {
	// Data (Attributes)
	private String name;
	private double hourlyRate;
	
	// Constructor
	public Worker(String name, double hourlyRate) {
		this.name = name;
		this.hourlyRate = hourlyRate;
	}//end constructor
	
	// Getters and Setters
	// Get Name - return name value
	public String getName() {
		return name;
	}//end getName()
}//end Worker
